class Genero:

    def __init__(self):
        self.__rock = ''
        self.__eletronica = ''
        self.__pop = ''

    def get_rock(self):
        return self.__rock

    def set_rock(self, rock):
        self.__rock = rock

    def get_eletronica(self):
        return self.__eletronica

    def set_eletronica(self, eletronica):
        self.__eletronica = eletronica

    def get_pop(self):
        return self.__pop

    def set_pop(self, pop):
        self.__pop = pop
